vector<ll> vv;

pair<ll, ll> solve(ll x, ll t) {
	ll ans = 0ll;
	ll cnt = 0ll;
	while(x) {
		if(x%t == 0)
			cnt++;
		ans += x/t;
		x/=t;
	}
	return make_pair(ans, cnt);
}

int main() {
	while(~scanf("%lld%lld", &n, &m)) {
		ll tmp = m;
		vv.clear();
		for(ll i=2; i*i<=m; i++) {
			if(m%i==0) {
				vv.push_back(i);
				while(m%i==0)	m/=i;
			}
		}
		if(m != 1)	vv.push_back(m);
		m = tmp;
		ll ans = INF;
		for(auto x : vv) {
			ans = min(ans, solve(n, x).fi/solve(m, x).se);
		}
		printf("%lld\n", ans);
	}
	return 0;
}

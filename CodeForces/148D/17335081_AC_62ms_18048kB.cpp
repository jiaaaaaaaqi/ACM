double dp[maxn][maxn][2];
bool vis[maxn][maxn][2];

double dfs(int i, int j, int p) {
	if(vis[i][j][p])	return dp[i][j][p];
	vis[i][j][p] = true;
	if(i == 0)	return dp[i][j][p] = 0;
	if(p == 0) {
		dp[i][j][p] = 1.0 * i / (i+j);
		if(j-1 >= 0)
			dp[i][j][p] += 1.0 * j / (i+j) * dfs(i, j-1, 1);
	} else {
		if(j-1>=0)
			dp[i][j][p] += 1.0 * i / (i+j-1) * dfs(i-1, j-1, 0);
		if(j >=2)
			dp[i][j][p] += 1.0 * (j-1) / (i+j-1) * dfs(i, j-2, 0);
		dp[i][j][p] *= 1.0 * j / (i+j);
	}
	return dp[i][j][p];
}

int main() {
	mes(vis, 0);
	while(~scanf("%d%d", &n, &m)) {
		double ans = dfs(n, m, 0);
		printf("%.12f\n", ans);
	}
	return 0;
}

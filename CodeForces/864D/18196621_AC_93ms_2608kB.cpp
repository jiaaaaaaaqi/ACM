int main() {
	scanf("%d", &n);
	queue<int> q;
	mes(cnt, 0);
	mes(vis, 0);
	while(!q.empty())	q.pop();
	for(int i=1; i<=n; i++) {
		scanf("%d", &a[i]);
		cnt[a[i]]++;
	}
	for(int i=1; i<=n; i++) {
		if(cnt[i] == 0)	q.push(i);
		if(cnt[i] == 1)	vis[i] = true;
	}
	printf("%d\n", q.size());
	for(int i=1; i<=n; i++) {
		if(cnt[a[i]] == 1)	continue;
		if(vis[a[i]]) {
			a[i] = q.front();
			q.pop();
			vis[a[i]] = true;
		} else {
			if(q.front() < a[i]) {
				cnt[a[i]] --;
				a[i] = q.front();
				q.pop();
				vis[a[i]] = true;
			} else {
				vis[a[i]] = true;
			}
		}
//		printf("%d %d\n", a[i], vis[a[i]]);
	}
	for(int i=1; i<=n; i++) {
		printf("%d%c", a[i], i==n ? '\n' : ' ');
	}
	return 0;
}

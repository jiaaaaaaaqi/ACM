char s[20];
int sg[5][5][5][5][5][5];

void dfs(int ans, int *w) {
	if(sg[w[1]][w[2]][w[3]][w[4]][w[5]][w[6]] != -1)
		return ;
	if(ans >= 31) {
		sg[w[1]][w[2]][w[3]][w[4]][w[5]][w[6]] = 0;
		return ;
	}
	for(int i=1; i<=6; i++) {
		if(w[i] && ans+i<31) {
			w[i]--;
			dfs(ans+i, w);
			if(sg[w[1]][w[2]][w[3]][w[4]][w[5]][w[6]] == 0) {
				w[i]++;
				sg[w[1]][w[2]][w[3]][w[4]][w[5]][w[6]] = 1;
				return ;
			}
			w[i]++;
		}
	}
	sg[w[1]][w[2]][w[3]][w[4]][w[5]][w[6]] = 0;
	return ;
}

int main() {
	mes(sg, -1);
	while(scanf("%s", s+1)) {
		int len = strlen(s+1);
		int cnt[10];
		for(int i=1; i<=6; i++)
			cnt[i] = 4;
		int ans = 0;
		for(int i=1; i<=len; i++) {
			cnt[s[i]-'0']--;
			ans += s[i]-'0';
		}
		int &x = sg[cnt[1]][cnt[2]][cnt[3]][cnt[4]][cnt[5]][cnt[6]];
		if(x == -1) 
			dfs(ans, cnt);
		printf("%s ", s+1);
		if(len&1)
			printf("%s\n", x ? "B" : "A");
		else
			printf("%s\n", x ? "A" : "B");
		mes(s, 0);
	}
    return 0;
}

char s[maxn][maxn];
vector<vector<int> > sg(maxn, vector<int>(maxn, -1));

void dfs(int x, int y) {
	if(sg[x][y] != -1)	return ;
	vector<int> vis(maxn*maxn, false);
	if(x == 1) {
		for(int j=1; j<y; j++) {
			dfs(x, j);
			vis[sg[x][j]] = true;
		}
	} else if(y == 1) {
		for(int i=1; i<x; i++) {
			dfs(i, y);
			vis[sg[i][y]] = true;
		}
	} else {
		for(int i=1; i<x; i++) {
			for(int j=1; j<y; j++) {
				dfs(i, y);
				dfs(x, j);
				vis[sg[i][y] ^ sg[x][j]] = true;
			}
		}
	}
	for(int i=0; ; i++) {
		if(!vis[i]) {
			sg[x][y] = i;
			return ;
		}
	}
}

void handle() {
	sg[1][1] = 0;
	for(int i=1; i<=50; i++) {
		for(int j=1; j<=50; j++) {
			if(sg[i][j] == -1)	dfs(i, j);
		}
	}
}

int main() {
	handle();
	while(scanf("%d%d", &n, &m), n||m) {
		int ans = 0;
		for(int i=1; i<=n; i++) {
			scanf("%s", s[i]+1);
			for(int j=1; j<=m; j++) {
				if(s[i][j] == '#') {
					ans ^= sg[i][j];
				}
			}
		}
		printf("%s\n", ans ? "John" : "Jack");
	}
	return 0;
}

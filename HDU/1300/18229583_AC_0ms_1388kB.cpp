int n, m;
int cas, tol, T;

int sum[maxn], p[maxn], q[maxn], dp[maxn];

int getup(int i, int j) {
	return dp[i] - dp[j];
}

int getdown(int i, int j) {
	return sum[i] - sum[j];
}

int getdp(int i, int j) {
	return (sum[i] - sum[j] + 10) * p[i];
}

int main() {
	scanf("%d", &T);
	while(T--) {
		scanf("%d", &n);
		for(int i=1; i<=n; i++) {
			scanf("%d%d", &sum[i], &p[i]);
			sum[i] += sum[i-1];
		}
		int head, tail;
		head = tail = 0;
		mes(dp, 0);
		q[0] = 0;
		for(int i=1; i<=n; i++) {
			while(head+1<=tail && getup(q[head+1], q[head]) <= p[i] * getdown(q[head+1], q[head]))
				head++;
			dp[i] = dp[q[head]] + getdp(i, q[head]);
//			printf("dp[%d] = %d\n", i, dp[i]);
			while(head+1<=tail && getup(q[tail], q[tail-1]) * getdown(i, q[tail]) >= getup(i, q[tail]) * getdown(q[tail], q[tail-1]))
				tail--;
			q[++tail] = i;
		}
		printf("%d\n", dp[n]);
	}
	return 0;
}

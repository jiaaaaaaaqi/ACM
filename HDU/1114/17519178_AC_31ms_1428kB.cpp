const int    maxn = 1e4 + 10;
const int    maxm = 1e3 + 10;
const ll     mod  = 1e9 + 7;
const ll     INF  = 1e18 + 100;
const int    inf  = 0x3f3f3f3f;
const double pi   = acos(-1.0);
const double eps  = 1e-8;
using namespace std;

int n, m;
int cas, tol, T;

struct Node{
	int p, w;
} node[maxm];
int dp[maxn];

void init() {
	mes(dp, inf);
}

int main() {
	scanf("%d", &T);
	while(T--) {
		int a, b;
		scanf("%d%d%d", &a, &b, &n);
		m = b - a;
		init();
		dp[0] = 0;
		for(int i=1; i<=n; i++) {
			scanf("%d%d", &node[i].p, &node[i].w);
		}
		for(int i=1; i<=n; i++) {
			for(int j=node[i].w; j<=m; j++) {
				dp[j] = min(dp[j], dp[j-node[i].w] + node[i].p);
			}
		}
		if(dp[m] == inf)	printf("This is impossible.\n");
		else	printf("The minimum amount of money in the piggy-bank is %d.\n", dp[m]);
	}
	return 0;
}

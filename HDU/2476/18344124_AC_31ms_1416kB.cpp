char s1[maxn], s2[maxn];
int dp[maxn][maxn], f[maxn];

int main() {
	while(~scanf("%s%s", s1+1, s2+1)) {
		mes(f, 0);
		n = strlen(s1+1);
		for(int i=1; i<=n; i++) {
			dp[i][i] = 1;
		}
		for(int d=2; d<=n; d++) {
			for(int i=1, j=d; j<=n; i++, j++) {
				dp[i][j] = dp[i][j-1]+1;
				for(int k=i+1; k<=j; k++) {
					dp[i][j] = min(dp[i][j], dp[i+1][k]+dp[k+1][j]+(s2[i]!=s2[k]));
				}
			}
		}
		for(int i=1; i<=n; i++) {
			f[i] = dp[1][i];
			if(s1[i] == s2[i])	f[i] = f[i-1];
			for(int j=1; j<i; j++) {
				f[i] = min(f[i], f[j]+dp[j+1][i]);
			}
		}
		printf("%d\n", f[n]);
	}
	return 0;
}

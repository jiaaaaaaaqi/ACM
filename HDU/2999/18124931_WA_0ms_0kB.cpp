int n, m;
int cas, tol, T;

vector<int> a;
int sg[maxn];

void handle() {
	sg[0] = 0;
	for(int i=1; i<=1000; i++) {
		vector<bool> vis(maxn, false);
		for(auto v : a) {
			if(i-v < 0)	break;
			if(i != v)
				vis[sg[i-v] ^ sg[v]] = true;
			else
				vis[sg[0]] = true;
		}
		for(int j=0; ; j++) {
			if(!vis[j]) {
				sg[i] = j;
				break;
			}
		}
	}
}

int main() {
	while(~scanf("%d", &tol)) {
		a.clear();
		mes(sg, -1);
		for(int i=1, x; i<=tol; i++) {
			scanf("%d", &x);
			a.push_back(x);
		}
		sort(a.begin(), a.end());
		a.erase(unique(a.begin(), a.end()), a.end());
		handle();
		scanf("%d", &T);
		while(T--) {
			int x;
			scanf("%d", &x);
			int ans = sg[x];
			printf("%d\n", ans ? 1 : 2);
		}
	}
	return 0;
}

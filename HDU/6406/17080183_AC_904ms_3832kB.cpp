int a[maxn];
int mx[maxn << 2];
int cnt[maxn << 2];

int query(int l, int r, int v, int rt) {
	if(l == r) {
		return mx[rt] > v;
	}
	int mid = (l + r) >> 1;
	if(mx[rt] <= v)	return 0;
	if(mx[rt<<1] <= v)	return query(mid+1, r, v, rt<<1|1);
	else	return cnt[rt] - cnt[rt<<1] + query(l, mid, v, rt<<1);
}

void pushup(int l, int r, int rt) {
	int mid = (l + r) >> 1;
	cnt[rt] = cnt[rt<<1] + query(mid+1, r, mx[rt<<1], rt<<1|1);
	mx[rt] = max(mx[rt<<1], mx[rt<<1|1]);
}

void build(int l, int r, int rt) {
	cnt[rt] = mx[rt] = 0;
	if(l == r) {
		scanf("%d", &a[l]);
		cnt[rt] = 1;
		mx[rt] = a[l];
		return ;
	}
	int mid = (l + r) >> 1;
	build(l, mid, rt<<1);
	build(mid+1, r, rt<<1|1);
	pushup(l, r, rt);
}

void update(int l, int r, int p, int v, int rt) {
	if(l == r) {
		mx[rt] = v;
		cnt[rt] = 1;
		return ;
	}
	int mid = (l + r) >> 1;
	if(p <= mid)
		update(l, mid, p, v, rt<<1);
	else
		update(mid+1, r, p, v, rt<<1|1);
	pushup(l, r, rt);
}


int dp[maxn][maxn];

int main() {
	while(~scanf("%d%d", &n, &m)) {
		mes(dp, 0);
		for(int i=1; i<=n; i++) {
			int k, id;
			scanf("%d%d", &k, &id);
			for(int j=0; j<=m; j++) {
				if(id == 0)
					dp[i][j] = -1;
				else
					dp[i][j] = dp[i-1][j];
			}
			while(k--) {
				int v, w;
				scanf("%d%d", &v, &w);
				for(int j=m; j>=v; j--) {
					if(id == 0) {
						if(dp[i][j-v] != -1)
							dp[i][j] = max(dp[i][j], dp[i][j-v] + w);
						if(dp[i-1][j-v] != -1)
							dp[i][j] = max(dp[i][j], dp[i-1][j-v] + w);
					} else if(id == 1) {
						if(dp[i-1][j-v] != -1)
							dp[i][j] = max(dp[i][j], dp[i-1][j-v] + w);							
					} else {
						if(dp[i][j-v] != -1)
							dp[i][j] = max(dp[i][j], dp[i][j-v] + w);
						if(dp[i-1][j-v] != -1)
							dp[i][j] = max(dp[i][j], dp[i-1][j-v] + w);
					}
				}
			}
		}
		printf("%d\n", dp[n][m]);
	}
	return 0;
}

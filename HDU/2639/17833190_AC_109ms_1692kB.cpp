using namespace std;

int n, m;
int cas, tol, T;

struct Node{
	int v, w;
} node[maxn];
ll dp[maxn][40];
ll a[40], b[40];

int main() {
	scanf("%d", &T);
	while(T--) {
		int k;
		scanf("%d%d%d", &n, &m, &k);
		for(int i=1; i<=n; i++) {
			scanf("%d", &node[i].w);
		}
		for(int i=1; i<=n; i++) {
			scanf("%d", &node[i].v);
		}
		mes(dp, 0);
		for(int i=1; i<=n; i++) {
			for(int j=m; j>=node[i].v; j--) {
				//dp[j] = max(dp[j], dp[j-node[i].v] + node[i].w;
				for(int jj=1; jj<=k; jj++) {
					a[jj] = dp[j][jj];
					b[jj] = dp[j-node[i].v][jj] + node[i].w;
				}
				a[k+1] = b[k+1] = -1;
				int l, r, x;
				x = l = r = 1;
				while(x<=k && (l<=k || r<=k)) {
					if(a[l] > b[r]) {
						dp[j][x] = a[l++];
					} else {
						dp[j][x] = b[r++];
					}
					if(dp[j][x] != dp[j][x-1])
						x++;
				}
			}
		}
		printf("%lld\n", max(dp[m][k], 0ll));
	}
	return 0;
}

int x[maxn];
int a[maxn << 2];

void pushup(int rt) {
	a[rt] = max(a[rt<<1], a[rt<<1|1]);
}

void build(int l, int r, int rt) {
	a[rt] = 0;
	if(l == r) {
		scanf("%d", &x[l]);
		a[rt] = x[l];
		return ;
	}
	int mid = (l + r) >> 1;
	build(l, mid, rt<<1);
	build(mid+1, r, rt<<1|1);
	pushup(rt);
}

int query(int l, int r, int pl, int pr, int v, int rt) {
	if(pl <= l && r <= pr) {
		if(a[rt] < v) {
			return r - l + 1;
		} else if(l == r) {
			return 0;
		}
	}
	int mid = (l + r) >> 1;
	int ans = 0;
	if(pl <= mid)
		ans += query(l, mid, pl, pr, v, rt<<1);
	if(pr > mid)
		ans += query(mid+1, r, pl, pr, v, rt<<1|1);
	return ans;
}

int main() {
	while(~scanf("%d", &n)) {
		build(1, n, 1);
		int ans = 0;
		for(int i=1; i<n; i++) {
			ans += query(1, n, i+1, n, x[i], 1);
		}
		int tmp = ans;
		for(int i=1; i<n; i++) {
			int cnt = 0;
			if(i != 1)
				cnt += query(1, n, 1, i-1, x[i], 1);
			cnt += query(1, n, i+1, n, x[i], 1);
			tmp += n - 1 - cnt - cnt;
			ans = min(ans, tmp);
		}
		printf("%d\n", ans);
	}
	return 0;
}

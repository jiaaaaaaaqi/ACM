using namespace std;

int n, m;
int cas, tol, T;

struct Node{
	int id;
	int w, v;
} node[maxm];
int dp[maxn][maxm];

int main() {
	int k;
	while(~scanf("%d%d%d", &n, &m, &k)) {
		mes(dp, 0);
		int x = 0;
		for(int i=1; i<=n; i++) {
			scanf("%d%d%d", &node[i].id, &node[i].w, &node[i].v);
			x += node[i].w;
		}
		m = min(x, m);
		int mx = 1<<k;
		mx--;
		for(int i=1; i<=n; i++) {
			for(int j=m; j>=node[i].w; j--) {
				for(int k=0; k<=mx; k++) {
					int tmp = k | (1<<node[i].id-1);
					dp[tmp][j] = max(dp[tmp][j], dp[k][j-node[i].w] + node[i].v);
				}
			}
		}
		if(dp[mx][m] == 0)	printf("Impossible\n");
		else	printf("%d\n", dp[mx][m]);
	}
	return 0;
}

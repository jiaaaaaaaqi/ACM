using namespace std;

int n, m;
int cas, tol, T;

struct Node{
	double x1, x2, x3;
} node[maxn][maxn];
double dp[maxn][maxn];

void init() {
	mes(dp, 0);
	mes(node, 0);
}

int main() {
	while(~scanf("%d%d", &n, &m)) {
		init();
		for(int i=1; i<=n; i++) {
			for(int j=1; j<=m; j++) {
				scanf("%lf%lf%lf", &node[i][j].x1, &node[i][j].x2, &node[i][j].x3);
			}
		}
		dp[n][m] = 0;
		double x1, x2, x3;
		for(int i=n; i>=1; i--) {
			for(int j=m; j>=1; j--) {
				if(i == n && j == m)	continue;
				x1 = node[i][j].x1;
				x2 = node[i][j].x2;
				x3 = node[i][j].x3;
				dp[i][j] = (x2 * dp[i][j+1] + x3 * dp[i+1][j] + 2.0) / (1 - x1);
			}
		}
		printf("%.3f\n", dp[1][1]);
	}
	return 0;
}

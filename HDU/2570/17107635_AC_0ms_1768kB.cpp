using namespace std;

int n, m;
int cas, tol, T;

int a[maxn];

int main() {
	scanf("%d", &T);
	while(T--) {
		mes(a, 0);
		int v, w;
		scanf("%d%d%d", &n, &v, &w);
		for(int i=1; i<=n; i++) {
			scanf("%d", &a[i]);
		}
		sort(a+1, a+1+n);
		int ans = 0;
		int cnt = 0;
		for(int i=1; i<=n; i++) {
			ans += a[i];
			cnt++;
			if(1.0*ans/cnt > 1.0*w+eps) {
				ans -= a[i];
				cnt--;
				break;
			}
		}
		if(cnt != 0) {
			printf("%d %.2f\n", cnt*v, 1.0*ans/cnt/100);
		} else {
			printf("0 0.00\n");
		}
	}
	return 0;
}

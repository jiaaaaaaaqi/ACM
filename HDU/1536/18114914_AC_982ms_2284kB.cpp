int n, m, k;
int cas, tol, T;

vector<int> a(maxn, 0);
vector<int> sg(maxn);

void handle() {
	sg.clear();
	sg[0] = 0;
	for(int i=1; i<=10000; i++) {
		vector<bool> vis(maxn);
		for(int j=1; j<=k; j++) {
			if(i-a[j] >= 0) {
				vis[sg[i-a[j]]] = true;
			}
		}
		for(int j=0; ; j++) {
			if(!vis[j]) {
				sg[i] = j;
				break;
			}
		}
	}
}

int main() {
	while(scanf("%d", &k), k) {
		a.clear();
		for(int i=1; i<=k; i++) {
			scanf("%d", &a[i]);
		}
		handle();
		scanf("%d", &T);
		while(T--) {
			scanf("%d", &n);
			int ans = 0;
			for(int i=1, x; i<=n; i++) {
				scanf("%d", &x);
				ans ^= sg[x];
			}
			printf("%s", ans ? "W" : "L");
		}
		printf("\n");
	}
	return 0;
}

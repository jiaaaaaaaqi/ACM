int n, m;
int cas, tol, T;

int sg[55][maxn];

void dfs(int x, int y) {
	if(sg[x][y] != -1)	return ;
	if(x == 0) {
		sg[x][y] = y&1;
		return ;
	}
	dfs(x-1, y);
	if(sg[x-1][y] == 0) {
		sg[x][y] = 1;
		return ;
	}
	dfs(x-1, y+1);
	if(sg[x-1][y+1] == 0) {
		sg[x][y] = 1;
		return ;
	}
	if(x > 1) {
		dfs(x-2, y+2);
		if(sg[x-2][y+2] == 0) {
			sg[x][y] = 1;
			return ;
		}
	}
	if(y) {
		dfs(x, y-1);
		if(sg[x][y-1] == 0) {
			sg[x][y] = 1;
			return ;
		}
	}
	sg[x][y] = 0;
	return ;
}

int main() {
	mes(sg, -1);
	sg[0][0] = 0;
	cas = 1;
	scanf("%d", &T);
	while(T--) {
		int ans = 0, cnt = 0;
		scanf("%d", &n);
		for(int i=1, x; i<=n; i++) {
			scanf("%d", &x);
			if(x > 1)
				ans += x+1;
			else 
				cnt++;
		}
		if(ans)
			ans--;
		if(sg[cnt][ans] == -1)
			dfs(cnt, ans);
		printf("Case #%d: %s\n", cas++, sg[cnt][ans] ? "Alice" : "Bob");
	}
	return 0;
}

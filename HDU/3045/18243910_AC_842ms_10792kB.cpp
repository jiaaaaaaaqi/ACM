ll getup(int i, int j) {
	return (dp[i]-sum[i]+i*a[i+1]) - (dp[j]-sum[j]+j*a[j+1]);
}

ll getdown(int i, int j) {
	return a[i+1] - a[j+1];
}

ll getdp(int i, int j) {
	return sum[i]-sum[j]-(i-j)*a[j+1];
}


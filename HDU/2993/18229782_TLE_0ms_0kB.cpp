int n, m;
int cas, tol, T;

int sum[maxn], q[maxn];

double getup(int i, int j) {
	return sum[i]-sum[j];
}

double getdown(int i, int j) {
	return i-j;
}

double getdp(int i, int j) {
	return 1.0*(sum[i]-sum[j])/(i-j);
}

int main() {
	while(~scanf("%d%d", &n, &m)) {
		for(int i=1; i<=n; i++) {
			scanf("%d", &sum[i]);
			sum[i] += sum[i-1];
		}
		int head, tail;
		head = tail = 0;
		q[0] = 0;
		double ans = 0;
		for(int i=m; i<=n; i++) {
			while(head+1<=tail && getup(q[head+1], q[head]) <= ans * getdown(q[head+1], q[head]))
				head++;
			ans = max(ans, getdp(i, q[head]));
			while(head+1<=tail && getup(i, q[tail]) * getdown(q[tail], q[tail-1]) <= getup(q[tail], q[tail-1]) * getdown(i, q[tail]))
				tail--;
			q[++tail] = i-m+1;
		}
		printf("%.2f\n", ans);
	}
	return 0;
}

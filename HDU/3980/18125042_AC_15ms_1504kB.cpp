int n, m;
int cas, tol, T;

int sg[maxn];

void dfs(int x) {
	if(sg[x] != -1)	return ;
	vector<bool> vis(maxn, false);
	for(int i=0; i+m<=x; i++) {
		dfs(i);
		dfs(x-i-m);
		vis[sg[i] ^ sg[x-i-m]] = true;
	}
	for(int i=0; ; i++) {
		if(!vis[i]) {
			sg[x] = i;
			return ;
		}
	}
}

int main() {
	cas = 1;
	scanf("%d", &T);
	while(T--) {
		scanf("%d%d", &n, &m);
		int ans;
		if(m > n) {
			ans = 1;
		} else {
			mes(sg, -1);
			sg[0] = 0;
			dfs(n-m);
			ans = sg[n-m];
		}
		printf("Case #%d: %s\n", cas++, !ans ? "aekdycoin" : "abcdxyzk");
	}
	return 0;
}

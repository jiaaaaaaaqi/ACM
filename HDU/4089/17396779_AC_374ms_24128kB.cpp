using namespace std;

int n, m, k;
int cas, tol, T;

double c[maxn];
double pp[maxn];
double dp[maxn][maxn];

int main() {
	double p1, p2, p3, p4;
	while(~scanf("%d%d%d%lf%lf%lf%lf", &n, &m, &k, &p1, &p2, &p3, &p4)) {
		if(fabs(p4) <= eps) {
			printf("0.00000\n");
			continue;
		}
		double p21 = p2 / (1 - p1);
		double p31 = p3 / (1 - p1);
		double p41 = p4 / (1 - p1);
		pp[0] = 1.0;
		for(int i=1; i<=n; i++)
			pp[i] = pp[i-1] * p21;
		dp[1][1] = p41 / (1 - p21);
		c[1] = p41;
		for(int i=2; i<=n; i++) {
			for(int j=1; j<=i; j++) {
				if(j == 1)		c[j] = p41;
				else if(j <= k)	c[j] = p31 * dp[i-1][j-1] + p41;
				else			c[j] = p31 * dp[i-1][j-1];
			}
			double tmp = 0;
			for(int j=1; j<=i; j++) {
				tmp += pp[i-j] * c[j];
			}
			dp[i][i] = tmp / (1.0 - pp[i]);
			dp[i][1] = p21 * dp[i][i] + c[1];
			for(int j=2; j<i; j++) {
				dp[i][j] = p21 * dp[i][j-1] + c[j];
			}
		}
		printf("%.5f\n", dp[n][m]);
	}
	return 0;
}

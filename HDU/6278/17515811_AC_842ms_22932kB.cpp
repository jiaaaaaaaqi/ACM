int n, m;
int cas, tol, T;

struct Node{
	int l, r;
	int cnt;
} node[maxn * 40];
int a[maxn];
int rt[maxn];

void init() {
	tol = 0;
	mes(a, 0);
	mes(rt, 0);
}

void update(int l, int r, int &x, int y, int p) {
	x = ++tol;
	node[x] = node[y];
	node[x].cnt++;
	if(l == r)	return ;
	int mid = l + r >> 1;
	if(p <= mid)
		update(l, mid, node[x].l, node[y].l, p);
	else
		update(mid+1, r, node[x].r, node[y].r, p);
}

int query(int l, int r, int x, int y, int k) {
	if(l == r) {
		return l < k ? node[y].cnt - node[x].cnt : 0;
	}
	int mid = l + r >> 1;
	int cnt = node[node[y].l].cnt - node[node[x].l].cnt;
	if(k <= mid)
		return query(l, mid, node[x].l, node[y].l, k);
	else
		return cnt + query(mid+1, r, node[x].r, node[y].r, k);
}

int main() {
	while(~scanf("%d%d", &n, &m)) {
		init();
		for(int i=1; i<=n; i++) {
			scanf("%d", &a[i]);
			update(1, n, rt[i], rt[i-1], a[i]);
		}
		while(m--) {
			int tl, tr;
			scanf("%d%d", &tl, &tr);
			int l = 1, r = n;
			int ans = 0;
			while(l <= r) {
				int mid = l + r >> 1;
				int tmp = query(1, n, rt[tl-1], rt[tr], mid);
				tmp = (tr - tl + 1) - tmp;
				if(tmp >= mid) {
					ans = mid;
					l = mid + 1;
				} else {
					r = mid - 1;
				}
			}
			printf("%d\n", ans);
		}
	}
	return 0;
}

struct Mat{
	ll mat[3][3];
	void init() {
		mes(mat, 0);
	}
};

int calc() {
	int x=0, y=0;
	do {
		y++;
		x = sqrt(1.0+d*y*y);
	} while(x*x-d*y*y!=1);
	return x;
}

Mat mmul(Mat A, Mat B) {
	Mat ans;
	ans.init();
	for(int i=1; i<=2; i++) {
		for(int j=1; j<=2; j++) {
			for(int k=1; k<=2; k++) {
				ans.mat[i][j] += A.mat[i][k] * B.mat[k][j];
				ans.mat[i][j] %= mod;
			}
		}
	}
	return ans;
}

Mat mpow(Mat A, int b) {
	Mat ans;
	ans.init();
	for(int i=1; i<=2; i++)
		ans.mat[i][i] = 1;
	while(b) {
		if(b & 1)
			ans = mmul(ans, A);
		A = mmul(A, A);
		b >>= 1;
	}
	return ans;
}


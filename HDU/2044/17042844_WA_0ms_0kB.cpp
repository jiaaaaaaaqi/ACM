ll dp[4][50];

void solve(int a, int &x, int &y) {
    if(a & 1)	x = 1;
    else	x = 2;
    y = (a + 1) / 2;
}

ll calc(int x1, int y1, int x2, int y2) {
    mes(dp, 0);
    int x = x1, y = y1;
    while(y <= y2) {
        if(x == 1) {
            if(x == x1 && y == y1)
                dp[x][y] = 1;
            else
                dp[x][y] = dp[1][y-1] + dp[2][y-1];
            x++;
        } else {
            if(x == x1 && y == y1)
                dp[x][y] = 1;
            else
                dp[x][y] = dp[1][y] + dp[2][y-1];
            x--;
            y++;
        }
    }
    return dp[x2][y2];
}

int main() {
    scanf("%d", &T);
    while(T--) {
        int a, b;
        scanf("%d%d", &a, &b);
        int x1, y1, x2, y2;
        solve(a, x1, y1);
        solve(b, x2, y2);
        printf("%d %d %d %d\n", x1, y1, x2, y2);
        printf("%lld\n", calc(x1, y1, x2, y2));
    }
    return 0;
}

using namespace std;

int n, m, k;
int cas, tol, T;

int a[maxn], sum[maxn];
deque<pair<int, int> > q;

int main() {
	scanf("%d", &T);
	while(T--) {
		while(!q.empty())	q.pop_back();
		mes(a, 0), mes(sum, 0);
		scanf("%d%d", &n, &k);
		for(int i=1; i<=n; i++) {
			scanf("%d", &a[i]);
			sum[i] = sum[i-1] + a[i];
		}
		for(int i=n+1; i<=2*n; i++) {
			sum[i] = sum[i-1] + a[i-n];
		}
		int l, r, ans;
		l = r = 0;
		ans = -inf;
		for(int i=1; i<=n+k; i++) {
			while(!q.empty() && sum[i-1] < q.back().se)	q.pop_back();
			while(!q.empty() && q.front().fi + k < i)	q.pop_front();
			q.push_back(make_pair(i-1, sum[i-1]));
			if(ans < sum[i] - q.front().se) {
				l = q.front().fi+1;
				r = i;
				ans = sum[i] - q.front().se;
				if(r > n)	r -= n;
			}
		}
		printf("%d %d %d\n", ans, l, r);
	}
	return 0;
}

double p[maxn];
double dp[1<<maxn];

int main() {
	while(~scanf("%d", &n)) {
		mes(p, 0);
		mes(dp, 0);
		ll mx = (1ll << n) - 1;
		for(int i=1; i<=n; i++)
			scanf("%lf", &p[i]);
		dp[mx] = 0;
		for(ll i=mx-1; i>=0; i--) {
			double tmp = 0.0;
			double sp = 0.0;
			for(int j=1; j<=n; j++) {
				if((i & (1<<(j-1))) == 0) {
					tmp += p[j] * dp[(i | (1<<(j-1)))];
					sp += p[j];
				}
			}
			dp[i] = (tmp+1.0) / sp;
			// printf("%lld %f\n", i, dp[i]);
		}
		printf("%.5f\n", dp[0]);
	}
	return 0;
}

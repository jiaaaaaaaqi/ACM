using namespace std;

int n, m;
int cas, tol, T;

struct Node{
	int l, r;
	int cnt;
} node[maxn * 40];
int rt[maxn];
int a[maxn];
vector<int> vv;

void init() {
	tol = 0;
	mes(a, 0);
	mes(rt, 0);
	vv.clear();
}

int getid(int x) {
	return lower_bound(vv.begin(), vv.end(), x) - vv.begin() + 1;
}

void update(int l, int r, int &x, int y, int pos) {
	tol++;
	node[tol] = node[y];
	node[tol].cnt++;
	x = tol;
	if(l == r)	return ;
	int mid = (l + r) >> 1;
	if(pos <= mid)
		update(l, mid, node[x].l, node[y].l, pos);
	else
		update(mid+1, r, node[x].r, node[y].r, pos);
}

int query(int l, int r, int x, int y, int k) {
	if(l == r) return l;
	int mid = (l + r) >> 1;
	int cnt = node[node[y].l].cnt - node[node[x].l].cnt;
	if(k <= cnt)
		return query(l, mid, node[x].l, node[y].l, k);
	else
		return query(mid+1, r, node[x].r, node[y].r, k-cnt);
}

int main() {
    scanf("%d", &T);
    while(T--) {
		init();
		scanf("%d%d", &n, &m);
		for(int i=1; i<=n; i++) {
			scanf("%d", &a[i]);
			vv.push_back(a[i]);
		}
		sort(vv.begin(), vv.end());
		vv.erase(unique(vv.begin(), vv.end()), vv.end());
		for(int i=1; i<=n; i++) {
			int pos = getid(a[i]);
			update(1, n, rt[i], rt[i-1], pos);
		}
		while(m--) {
			int l, r, k;
			scanf("%d%d%d", &l, &r, &k);
			int ans = query(1, n, rt[l-1], rt[r], k);
			printf("%d\n", vv[ans-1]);
		}
    }
    return 0;
}

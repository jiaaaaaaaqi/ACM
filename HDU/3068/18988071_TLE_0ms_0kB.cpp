char s[maxn], str[maxn];
int p[maxn];

void handle(char *s) {
	str[0] = '@';
	str[1] = '!';
	int len = strlen(s);
	for(int i=0; i<len; i++) {
		str[2*i+2] = s[i];
		str[2*i+3] = '!';
	}
	str[2*(len-1)+4] = '\0';
}

void manacher(char *s) {
	int len = strlen(s);
	int mx, id;
	mx = id = 0;
	for(int i=1; i<len; i++) {
		p[i] = mx>i ? min(p[2*id-1], mx-i) : 1;
		for(; s[i+p[i]] == s[i-p[i]]; p[i]++);
		if(i+p[i] > mx) {
			mx = p[i]+i;
			id = i;
		}
	}
}

int main() {
	while(~scanf("%s", s)) {
		handle(s);
		manacher(str);
		int ans = 0;
		int len = strlen(str);
		for(int i=0; i<len; i++) {
			ans = max(ans, p[i]-1);
		}
		cout << ans << endl;
	}
	return 0;
}

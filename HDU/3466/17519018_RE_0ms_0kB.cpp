int n, m;
int cas, tol, T;

struct Node{
	int p, q, v;
	bool operator < (Node a) const {
		return q-p < a.q-a.p;
	}
} node[maxn];
int dp[maxn];

void init() {
	mes(dp, 0);
}

int main() {
	while(~scanf("%d%d", &n, &m)) {
		init();
		for(int i=1; i<=n; i++) {
			scanf("%d%d%d", &node[i].p, &node[i].q, &node[i].v);
		}
		sort(node+1, node+1+n);
		for(int i=1; i<=n; i++) {
			for(int j=m; j>=node[i].q; j--) {
				dp[j] = max(dp[j], dp[j-node[i].p] + node[i].v);
			}
		}
		printf("%d\n", dp[m]);
	}
	return 0;
}

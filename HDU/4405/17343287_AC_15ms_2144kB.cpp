double dp[maxn];
map<int, int> mp;

void init() {
	for(int i=0; i<=n+6; i++)
		dp[i] = 0.0;
	mp.clear();
}

int main() {
	while(scanf("%d%d", &n, &m), n||m) {
		init();
		for(int i=1; i<=m; i++) {
			int a, b;
			scanf("%d%d", &a, &b);
			mp[a] = b;
		}
		for(int i=n-1; i>=0; i--) {
			if(mp.count(i)) {
				dp[i] = dp[mp[i]];
			} else {
				for(int j=i+1; j<=i+6; j++) {
					if(j > n)	break;
					dp[i] += dp[j];
				}
				dp[i] = dp[i] / 6.0;
				dp[i] += 1.0;
			}
		}
		printf("%.4f\n", dp[0]);
	}
	return 0;
}

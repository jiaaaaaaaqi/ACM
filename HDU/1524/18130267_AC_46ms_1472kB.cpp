int n, m;
int cas, tol, T;

vector<int> vv[maxn];
int sg[maxn];

void dfs(int u) {
	if(sg[u] != -1)	return ;
	vector<bool> vis(maxn, false);
	for(auto v : vv[u]) {
		dfs(v);
		vis[sg[v]] = true;
	}
	for(int i=0; ; i++) {
		if(!vis[i]) {
			sg[u] = i;
			break;
		}
	}
	return ;
}

int main() {
	while(~scanf("%d", &n)) {
		for(int i=1, x; i<=n; i++) {
			vv[i].clear();
			scanf("%d", &x);
			if(x == 0)
				sg[i] = 0;
			for(int j=1, y; j<=x; j++) {
				scanf("%d", &y);
				vv[i].push_back(y+1);
			}
		}
		mes(sg, -1);
		for(int i=1; i<=n; i++) {
			if(sg[i] == -1) 
				dfs(i);
		}
		while(scanf("%d", &T), T) {
			int ans = 0;
			for(int i=1, x; i<=T; i++) {
				scanf("%d", &x);
				ans ^= sg[x+1];
			}
			printf("%s\n", ans ? "WIN" : "LOSE");
		}
	}
	return 0;
}

struct Node{
	int h, a, c;
	bool operator < (Node x) const {
		return a < x.a;
	}
} node[500];
set<int> dp[2];

int main() {
	scanf("%d", &n);
	for(int i=1; i<=n; i++) {
		scanf("%d%d%d", &node[i].h, &node[i].a, &node[i].c);
	}
	sort(node+1, node+1+n);
	int ans = 0;
	dp[0].clear();
	dp[1].clear();
	dp[0].insert(0);
	int flag = 0;
	for(int i=1; i<=n; i++) {
		flag = !flag;
		dp[flag].clear();
		for(set<int>::iterator it = dp[!flag].begin(); it != dp[!flag].end(); it++) {
			int k = *it;
			for(int j=0; j<=node[i].c; j++) {
				if(k + j*node[i].h > node[i].a)	break;
				dp[flag].insert(k + j*node[i].h);
				ans = max(ans, k + j*node[i].h);
			}
		}
	}
	printf("%d\n", ans);
	return 0;
}

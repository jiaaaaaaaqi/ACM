struct Node {
    int num, val;
} node[maxn];
int dp[maxn];
int ans;

void zero_one_backpack(int w) {
    for(int i=m; i>=w; i--) {
        dp[i] = (dp[i] || dp[i-w]);
        if(dp[i]) {
            ans = max(ans, i);
        }
    }
}

void complete_backpack(int w) {
    for(int i=w; i<=m; i++) {
        dp[i] = (dp[i] || dp[i-w]);
        if(dp[i]) {
            ans = max(ans, i);
        }
    }
}

int main() {
    while(~scanf("%d%d", &m, &n)) {
        for(int i=1; i<=n; i++) {
            scanf("%d%d", &node[i].num, &node[i].val);
        }
        ans = 0;
        mes(dp, 0);
        dp[0] = 1;
        for(int i=1; i<=n; i++) {
			if(node[i].num <= 0)	continue;
            if(node[i].val * node[i].num >= m) {
                complete_backpack(node[i].val);
            } else {
				int k;
				for(k=1; k<node[i].num; k<<=1) {
					zero_one_backpack(k*node[i].val);
					node[i].num -= k;
				}
				zero_one_backpack(k*node[i].val);
            }
        }
        printf("%d\n", ans);
    }
    return 0;
}

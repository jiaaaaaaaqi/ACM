int sgn(double x) {
	if(fabs(x) < eps)	return 0;
	return x > 0 ? 1 : -1;
}

struct Point{
	int id;
	double x, y;
	Point() {}
	Point(int _id, double _x, double _y) {
		id = _id;
		x = _x, y = _y;
	}
	Point operator - (Point a) {
		return Point(id, x-a.x, y-a.y);
	}
	bool operator < (Point a) {
		return sgn(y-a.y) == 0 ? sgn(x-a.x)<0 : y<a.y;
	}
	double operator ^ (Point a) {
		return x*a.y - a.x*y;
	}
	double distance(Point a) {
		return hypot(x-a.x, y-a.y);
	}
} point[100];

struct cmp {
	Point p;
	cmp(Point _p) {
		p = _p;
	}
	bool operator()(Point _a, Point _b) const {
		Point a = _a, b = _b;
		int d = sgn((a-p) ^ (b-p));
		if(d == 0) {
			return sgn(a.distance(p) - a.distance(p)) < 0;
		} else {
			return d > 0;
		}
	}
};
int ans[105];


struct Point {
    double x, y;
    Point() {}
    Point(double a, double b) {
        x = a, y = b;
    }
    Point operator - (Point a) const {
        return Point(x-a.x, y-a.y);
    }
    double operator ^ (Point a) const {
        return x*a.y - y*a.x;
    }
};

struct Line {
    Point s, t;
    Line() {}
    Line(Point a, Point b) {
        s = a, t = b;
    }
    bool operator < (Line a) const {
		return s.x < a.s.x;
    }
} line[maxn];
int cnt[maxn];
int ans[maxn];

bool check(Point p, Line l) {
    double ans = (p - l.t) ^ (p-l.s);
    return ans > 0;
}

int main() {
    double x1, x2, y1, y2;
    while(scanf("%d", &n), n) {
        scanf("%d", &m);
        mes(cnt, 0), mes(ans, 0);
        scanf("%lf%lf%lf%lf", &x1, &y1, &x2, &y2);
        for(int i=1; i<=n; i++) {
            double ui, li;
            scanf("%lf%lf", &ui, &li);
            line[i] = Line(Point(ui, y1), Point(li, y2));
        }
        n++;
        line[n] = Line(Point(x2, y1), Point(x2, y2));
        sort(line+1, line+1+n);
        while(m--) {
            double x, y;
            scanf("%lf%lf", &x, &y);
            int l = 1, r = n;
            while(l < r) {
                int mid = l + r >> 1;
                if(check(Point(x, y), line[mid])) {
                    r = mid;
                } else {
                    l = mid + 1;
                }
            }
            ans[cnt[l-1]]--;
            cnt[l-1]++;
            ans[cnt[l-1]]++;
        }
        cout << "Box" << endl;
        tol = 0;
        for(int i=1; i<=n; i++) {
			if(ans[i] == 0)	continue;
			printf("%d: %d\n", i, ans[i]);
			tol += i*ans[i];
			if(tol == m)	break;
        }
    }
    return 0;
}

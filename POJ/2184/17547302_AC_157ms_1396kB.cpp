struct Node {
    int s, f;
    bool operator < (Node a) const {
        return s > a.s;
    }
} node[105];
int dp[2][maxn];

int main() {
    scanf("%d", &n);
    for(int i=1; i<=n; i++) {
        scanf("%d%d", &node[i].s, &node[i].f);
    }
    sort(node+1, node+1+n);
//    for(int i=1; i<=n; i++) {
//        printf("!!!%d %d\n", node[i].s, node[i].f);
//    }
    int mx = 0;
    dp[0][0] = 0;
    int flag = 0;
    for(int i=1; i<=n; i++) {
        flag = !flag;
        for(int j=0; j<maxn; j++)
			dp[flag][j] = -inf;
		for(int j=mx; j>=0; j--) {
            if(dp[!flag][j] == -inf)	continue;
            dp[flag][j] = max(dp[flag][j], dp[!flag][j]);
//            printf("dp[%d][%d] = %d\n", i, j, dp[flag][j]);
		}
        for(int j=mx; j>=0; j--) {
            if(dp[!flag][j] == -inf)	continue;
            if(j + node[i].s < 0)		break;
            dp[flag][j+node[i].s] = max(dp[flag][j+node[i].s], dp[!flag][j] + node[i].f);
//            printf("dp[%d][%d] = %d\n", i, j+node[i].s, dp[flag][j+node[i].s]);
            mx = max(mx, j+node[i].s);
        }
//        printf("\n");
    }
    int ans = 0;
    for(int i=0; i<=mx; i++) {
        if(dp[flag][i] < 0)	continue;
        ans = max(ans, i+dp[flag][i]);
    }
    printf("%d\n", ans);
    return 0;
}

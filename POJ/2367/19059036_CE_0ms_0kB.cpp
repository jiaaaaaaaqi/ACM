vector<int> vv[maxn];
int ind[maxn], a[maxn];

void topu() {
	priority_queue<int, vector<int>, greater<int> >q;
	for(int i=1; i<=n; i++) {
		if(ind[i] == 0)	q.push(i);
	}
	tol = 0;
	while(!q.empty()) {
		int u = q.top();
		a[++tol] = u;
		q.pop();
		for(auto v : vv[u]) {
			ind[v]--;
			if(ind[v] == 0) {
				q.push(v);
			}
		}
	}
}

int main() {
	while(~scanf("%d", &n)) {
		for(int i=1; i<=n; i++) {
			vv[i].clear();
			ind[i] = 0;
		}
		for(int u=1, v; u<=n; u++) {
			while(scanf("%d", &v), v) {
				vv[u].push_back(v);
				ind[v]++;
			}
		}
		topu();
		for(int i=1; i<=n; i++) {
			printf("%d%c", a[i], i==n ? '\n' : ' ');
		}
	}
	return 0;
}

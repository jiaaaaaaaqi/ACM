int a[50], b[50];
int dp[2][maxn];

int main() {
	scanf("%d%d", &n, &m);
	for(int i=1; i<=n; i++)
		scanf("%d", &a[i]);
	for(int i=1; i<=m; i++)
		scanf("%d", &b[i]);
	int mid = 2e4;
	int mn, mx;
	mn = mx = mid;
	dp[0][mid] = 1;
	int flag = 0;
	for(int i=1; i<=m; i++) {
		flag = !flag;
		for(int k=mn; k<=mx; k++) {
			dp[flag][k] = 0;
		}
		for(int k=mn; k<=mx; k++) {
			if(dp[!flag][k]) {
				for(int j=1; j<=n; j++) {
					dp[flag][k+a[j]*b[i]] += dp[!flag][k];
					mx = max(mx, k+a[j]*b[i]);
					mn = min(mn, k+a[j]*b[i]);
				}
			}
		}
	}
	printf("%d\n", dp[flag][mid]);
	return 0;
}

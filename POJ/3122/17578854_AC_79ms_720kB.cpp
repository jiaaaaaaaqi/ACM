const double pi   = acos(-1.0);
const double eps  = 1e-5;
using namespace std;

int n, m;
int cas, tol, T;

double a[maxn];

bool check(double x) {
	int cnt = 0;
	for(int i=1; i<=n; i++) {
		cnt += (int) floor(a[i] / x);
		if(cnt >= m)	return true;
	}
	return false;
}

int main() {
	scanf("%d", &T);
	while(T--) {
		scanf("%d%d", &n, &m);
		m++;
		double mx = 0.0;
		for(int i=1; i<=n; i++) {
			scanf("%lf", &a[i]);
			a[i] = pi * a[i] * a[i];
			mx = max(mx, a[i]);
		}
		double l = 0.0;
		double r = mx;
		double ans = 0.0;
		while(abs(l-r) > eps) {
			double mid = (l + r) / 2;
			if(check(mid)) {
				ans = mid;
				l = mid + eps;
			} else {
				r = mid - eps;
			}
		}
		printf("%.4f\n", ans);
	}
	return 0;
}

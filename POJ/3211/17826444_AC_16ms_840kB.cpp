int n, m;
int cas, tol, T;
bool dp[maxn];
vector<int> vv[15];
map<string, int> mp;

void init() {
	for(int i=1; i<=10; i++)	vv[i].clear();
	mp.clear();
}

int solve(vector<int> v) {
	mes(dp, 0);
	dp[0] = 1;
	int mx = 0;
	int sum = 0;
	int len = v.size();
	for(int i=0; i<len; i++) {
		int u = v[i];
		sum += u;
		for(int i=mx; i>=0; i--) {
			if(dp[i]) {
				dp[i+u] = true;
				mx = max(mx, i+u);
			}
		}
	}
	int ans = sum;
	for(int i=0; i<=mx; i++) {
		if(dp[i])
			ans = min(ans, max(i, sum-i));
	}
	return ans;
}

int main() {
	while(scanf("%d%d", &n, &m), n||m) {
		init();
		for(int i=1; i<=n; i++) {
			string s;
			cin >> s;
			mp[s] = i;
		}
		for(int i=1; i<=m; i++) {
			int x;
			string s;
			cin >> x >> s;
			vv[mp[s]].push_back(x);
		}
		int ans = 0;
		for(int i=1; i<=n; i++) {
			ans += solve(vv[i]);
		}
		cout << ans << endl;
	}
	return 0;
}

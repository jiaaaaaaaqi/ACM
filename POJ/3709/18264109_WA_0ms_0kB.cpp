using namespace std;

int n, m;
int cas, tol, T;

int a[maxn], q[maxn];
ll sum[maxn], dp[maxn];

ll getup(int i, int j) {
	return (dp[i]-sum[i]+i*a[i+1]) - (dp[j]-sum[j]+j*a[j+1]);
}

ll getdown(int i, int j) {
	return a[i+1]-a[j+1];
}

ll getdp(int i, int j) {
	return sum[i] - sum[j] - (i-j)*a[j+1];
}

int main() {
	scanf("%d", &T);
	while(T--) {
		mes(dp, 0);
		scanf("%d%d", &n, &m);
		sum[0] = 0;
		for(int i=1; i<=n; i++)	{
			scanf("%d", &a[i]);
			sum[i] = sum[i-1] + a[i];
		}
		for(int i=m; i<=n; i++)	dp[i] = sum[i] - i*a[1];
		int head, tail;
		head = tail = 0;
		q[0] = 0;
		q[++tail] = m;
		int j = m+1;
		for(int i=2*m; i<=n; i++) {
			while(head+1<=tail && getup(q[head+1], q[head]) < i*getdown(q[head+1], q[head]))
				head++;
			dp[i] = dp[q[head]] + getdp(i, q[head]);
//			printf("%d, dp[%d] = %lld\n", q[head], i, dp[i]);
			while(head+1<=tail && getup(q[tail], q[tail-1])*getdown(j, q[tail]) >= getup(j, q[tail])*getdown(q[tail], q[tail-1]))
				tail--;
			q[++tail] = j++;
		}
		printf("%lld\n", dp[n]);
	}
	return 0;
}

	scanf("%d", &T);
	while(T--) {
		scanf("%d%d", &n, &m);
		sum[0] = 0;
		for(int i=1; i<=n; i++)	{
			scanf("%d", &a[i]);
			sum[i] = sum[i-1] + a[i];
		}
		for(int i=m; i<=n; i++)	dp[i] = sum[i] - i*a[1];
		int j = 1;
		for(int i=m; i<=n; i++) {
			int id;
			for(int k=m; k<j; k++) {
				dp[i] = min(dp[i], dp[k] + (sum[i]-sum[k]-(i-k)*a[k+1]));
				if(dp[i] == dp[k] + (sum[i]-sum[k]-(i-k)*a[k+1]))
					id = k;
			}
			j++;
//			printf("%d dp[%d] = %lld\n", id, i, dp[i]);
		}
		printf("%lld\n", dp[n]);
	}

typedef unsigned long long int ull;
typedef long long int ll;
const int    maxn = 1e5 + 10;
const int    maxm = 1e5 + 10;
const ll     mod  = 1e9 + 7;
const ll     INF  = 1e18 + 100;
const int    inf  = 0x3f3f3f3f;
const double pi   = acos(-1.0);
const double eps  = 1e-8;
using namespace std;

int n, m;
int cas, tol, T;

struct Node{
	int v, c;
} node[120];
int dp[maxn];
int dp2[maxn];

void cpbp(int v) {
	for(int i=v; i<=m; i++) {
		dp[i] = min(dp[i], dp[i-v] + 1);
	}
}

void zobp(int k, int v) {
	for(int i=m; i>=v; i--) {
		dp[i] = min(dp[i], dp[i-v] + k);
	}
}

int main() {
	scanf("%d%d", &n, &T);
	for(int i=1; i<=n; i++) {
		scanf("%d", &node[i].v);
		m = max(node[i].v*node[i].v, m);
	}
	for(int i=1; i<=n; i++) {
		scanf("%d", &node[i].c);
	}
	mes(dp, inf);
	dp[0] = 0;
	for(int i=1; i<=n; i++) {
		if(node[i].v*node[i].c >= m) {
			cpbp(node[i].v);
		} else {
			int k = 1;
			for(; k<=node[i].c; k <<= 1) {
				zobp(k, node[i].v*k);
				node[i].c -= k;
			}
			zobp(node[i].c, node[i].v*node[i].c);
		}
	}
	mes(dp2, inf);
	dp2[0] = 0;
	for(int i=1; i<=n; i++) {
		for(int j=node[i].v; j<=m; j++) {
			dp2[j] = min(dp2[j], dp2[j-node[i].v] + 1);
		}
	}
	int ans = inf;
	for(int i=T; i<=m; i++) {
		ans = min(ans, dp[i] + dp2[i-T]);
	}
	if(ans < inf)	printf("%d\n", ans);
	else	printf("-1\n");
	return 0;
}

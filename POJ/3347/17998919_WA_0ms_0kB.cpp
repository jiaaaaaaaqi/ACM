int n, m;
int cas, tol, T;

struct Node{
	double l, r, s;
} node[maxn], ans[maxn];

int sgn(double x) {
	if(fabs(x) == 0)	return 0;
	return x > 0 ? 1 : -1;
}

int main() {
	double sq2 = sqrt(2.0);
	while(scanf("%d", &n), n) {
		mes(node, 0), mes(ans, 0);
		for(int i=1; i<=n; i++) {
			scanf("%lf", &node[i].s);
			node[i].l = 0.0;
			for(int j=1; j<i; j++) {
				double res = node[j].r - fabs(node[j].s - node[i].s);
				node[i].l = max(node[i].l, res);
			}
			node[i].r = node[i].l + node[i].s * sq2;
		}
		for(int i=1; i<=n; i++) {
			ans[i] = node[i];
			for(int j=1; j<i; j++) {
				if(node[j].s > node[i].s) {
					if(node[j].r > node[i].l) {
						ans[i].l = max(ans[i].l, node[j].r);
					}
				}
			}
			for(int j=i+1; j<=n; j++) {
				if(node[j].s > node[i].s) {
					if(node[j].l < node[i].r) {
						ans[i].r = min(ans[i].r, node[j].l);
					}
				}
			}
		}
		vector<int> vv;
		vv.clear();
		for(int i=1; i<=n; i++) {
			if(sgn(ans[i].l - ans[i].r) < 0) {
				vv.push_back(i);
			}
		}
		for(int i=0; i<vv.size(); i++) {
			printf("%d%c", vv[i], i==vv.size()-1 ? '\n' : ' ');
		}
	}
	return 0;
}

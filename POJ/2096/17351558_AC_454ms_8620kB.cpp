int n, m;
int cas, tol, T;

double dp[maxn][maxn];

void init() {
	mes(dp, 0);
}

int main() {
	init();
	scanf("%d%d", &n, &m);
	double total = 1.0 * n * m;
	dp[n][m] = 0;
	for(int i=n; i>=0; i--) {
		for(int j=m; j>=0; j--) {
			if(i == n && j == m)	continue;
			double x1 = 1.0 * (n-i) * j / total;
			double x2 = 1.0 * (m-j) * i / total;
			double x3 = 1.0 * (n-i) * (m-j) / total;
			double x4 = 1.0 * i * j / total;
			dp[i][j] = (x1*dp[i+1][j] + x2*dp[i][j+1] + x3*dp[i+1][j+1] + 1) / (1 - x4);
//			printf("dp[%d][%d] = %f\n", i, j, dp[i][j]);
		}
	}
	printf("%.4f\n", dp[0][0]);
	return 0;
}

typedef unsigned long long int ull;
typedef long long int ll;
const int    maxn = 11;
const int    maxm = 1 << 11;
const ll     mod  = 1e9 + 7;
const ll     INF  = 1e18 + 100;
const int    inf  = 0x3f3f3f3f;
const double pi   = acos(-1.0);
const double eps  = 1e-8;
using namespace std;

int n, m;
int cas, tol, T;

int a[maxn];
ll dp[maxm];
vector<int> vv;
bool vis[maxm];
int c1, c2;

bool ok(int st) {
    mes(vis, 0);
    vis[0] = 1;
    int sum = 0;
    for(int i=1; i<=n; i++) {
        if(st & (1<<(i-1))) {
            sum += a[i];
            for(int j=c1; j>=a[i]; j--) {
                if(vis[j - a[i]]) {
                    vis[j] = 1;
                }
            }
        }
    }
    if(sum > c1 + c2)	return false;
    for(int i=0; i<=c1; i++) {
        if(sum - i <= c2 && vis[i])
            return true;
    }
    return false;
}

void handle() {
    int mx = (1<<n)-1;
    for(int i=1; i<=mx; i++) {
        if(ok(i))
            vv.push_back(i);
    }
}

void out(int x) {
    if(x > 1)
        out(x/2);
    putchar(x%2 + '0');
}

int main() {
	cas = 1;
    scanf("%d", &T);
    while(T--) {
        vv.clear();
        mes(a, 0);
        mes(dp, inf);
        scanf("%d%d%d", &n, &c1, &c2);
        for(int i=1; i<=n; i++) {
            scanf("%d", &a[i]);
        }
        handle();
        dp[0] = 0;
        int mx = (1<<n) - 1;
        for(auto i : vv) {
            for(int j=mx; j>=0; j--) {
                if(i & j)	continue;
                if(dp[j] == inf)	continue;
                dp[i | j] = min(dp[i | j], dp[j]+1);
//				printf("dp[%d] = %d\n", i|j, dp[i|j]);
            }
        }
        printf("Scenario #%d:\n", cas++);
        printf("%d\n", dp[mx]);
        if(T)	printf("\n");
    }
    return 0;
}

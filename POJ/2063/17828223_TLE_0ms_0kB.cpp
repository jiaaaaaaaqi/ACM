int dp[maxn];

int main() {
	scanf("%d", &T);
	while(T--) {
		int t;
		scanf("%d%d", &m, &t);
		scanf("%d", &n);
		for(int i=1; i<=n; i++) {
			scanf("%d%d", &node[i].v, &node[i].w);
		}
		while(t--) {
			for(int i=0; i<=m; i++) {
				dp[i] = 0.0;
			}
			for(int i=1; i<=n; i++) {
				for(int j=node[i].v; j<=m; j++) {
					dp[j] = max(dp[j-node[i].v] + node[i].w, dp[j]);
				}
			}
			m += dp[m];
		}
		printf("%d\n", m);
	}
	return 0;
}

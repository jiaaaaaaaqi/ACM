int n, m;
int cas, tol, T;

int a[maxn];
int dp[2][maxn], sum[maxn];

int main() {
	scanf("%d%d", &n, &m);
	sum[0] = 0;
	for(int i=1; i<=n; i++) {
		scanf("%d", &a[i]);
		sum[i] = sum[i-1] + a[i];
	}
	for(int i=1; i<=n; i++) {
		int mid = (i+1)>>1;
//		dp[1][i] = 0;
//		for(int j=1; j<=i; j++) {
//			dp[1][i] += abs(a[j] - a[mid]);
//		}
		dp[1][i] = (mid*a[mid]-sum[mid]) + (sum[i]-sum[mid] - (i-mid)*a[mid]);
	}
	int flag = 1;
	for(int k=2; k<=m; k++) {
		flag = !flag;
		mes(dp[flag], inf);
		for(int i=k; i<=n; i++) {
			for(int j=k-1; j<=i; j++) {
				int ans = dp[!flag][j];
				int mid = (i + j + 1)>>1;
//				for(int z=j+1; z<=i; z++) {
//					ans += abs(a[z] - a[mid]);
//				}
				ans += ((mid-j)*a[mid]-(sum[mid]-sum[j])) + ((sum[i]-sum[mid])-(i-mid)*a[mid]);
				dp[flag][i] = min(dp[flag][i], ans);
			}
//			printf("dp[%d][%d] = %d\n", k, i, dp[flag][i]);
		}
	}
	printf("%d\n", dp[flag][n]);
	return 0;
}

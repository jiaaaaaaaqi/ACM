int n, m;
int cas, tol, T;

int ans;
char maps[maxn][maxn];
bool vis[maxn][maxn];
int dis[4][2] = {1, 0, -1, 0, 0, 1, 0, -1};

bool check(int i,int j) {
	if(i <= 0 || j <= 0)	return 0;
	if(i > n || j > m)		return 0;
	if(vis[i][j])			return 0;
	if(maps[i][j] == '#')	return 0;
	return 1;
}

void dfs(int x, int y) {
	if(!check(x, y)) return ;
	ans++;
	vis[x][y] = 1;
	for(int i=0; i<4; i++) {
		int xx = x + dis[i][0];
		int yy = y + dis[i][1];
		dfs(xx, yy);
	}
}

int main() {
	while(scanf("%d%d", &m, &n), n||m) {
		mes(maps, 0);
		mes(vis, 0);
		ans = 0;
		int bex, bey;
		for(int i=1; i<=n; i++) {
			scanf("%s", maps[i]+1);
			for(int j=1; j<=m; j++) {
				if(maps[i][j] == '@') {
					bex = i;
					bey = j;
				}
			}
		}
		dfs(bex, bey);
		printf("%d\n", ans);
	}
	return 0;
}

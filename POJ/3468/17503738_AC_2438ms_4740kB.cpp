int n, m;
int cas, tol, T;

struct Seg{
	ll sum;
	ll lazy;
} seg[maxn << 2];

void build(int l, int r, int rt) {
	seg[rt].sum = seg[rt].lazy = 0;
	int mid = l + r >> 1;
	if(l == r) {
		scanf("%lld", &seg[rt].sum);
		return ;
	}
	build(l, mid, rt<<1);
	build(mid+1, r, rt<<1|1);
	seg[rt].sum = seg[rt<<1].sum + seg[rt<<1|1].sum;
}

void update(int l, int r, int pl, int pr, ll v, int rt) {
	seg[rt].sum += 1ll * (min(r, pr) - max(l, pl) + 1) * v;
	if(pl <= l && r <= pr) {
		seg[rt].lazy += v;
		return ;
	}
	int mid = l + r >> 1;
	if(pl <= mid)
		update(l, mid, pl, pr, v, rt<<1);
	if(pr > mid)
		update(mid+1, r, pl, pr, v, rt<<1|1);
}

ll query(int l, int r, int pl, int pr, int rt) {
	if(pl <= l && r <= pr)	return seg[rt].sum;
	ll ans = 1ll * (min(r, pr) - max(l, pl) + 1) * seg[rt].lazy;
	int mid = l + r >> 1;
	if(pl <= mid)
		ans += query(l, mid, pl, pr, rt<<1);
	if(pr > mid)
		ans += query(mid+1, r, pl, pr, rt<<1|1);
	return ans;
}

int main() {
	while(~scanf("%d%d", &n, &m)) {
		build(1, n, 1);
		char s[5];
		while(m--) {
			scanf("%s", s+1);
			if(s[1] == 'Q') {
				int l, r;
				scanf("%d%d", &l, &r);
				printf("%lld\n", query(1, n, l, r, 1));
			} else {
				int l, r;
				ll v;
				scanf("%d%d%lld", &l, &r, &v);
				update(1, n, l, r, v, 1);
			}
		}
	}
	return 0;
}

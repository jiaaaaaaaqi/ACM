int main() {
	while(scanf("%d%d%d", &m, &n, &T), n||m||T) {
		mes(p, 0);
		mes(dp, 0);
		for(int i=1; i<=n; i++) {
			for(int j=1; j<=m; j++) {
				scanf("%lf", &p[i][j]);
			}
		}
		for(int i=1; i<=n; i++) {
			dp[i][0][0] = 1.0;
			for(int j=1; j<=m; j++) {
				for(int k=0; k<T; k++) {
					dp[i][j][k] = dp[i][j-1][k] * (1 - p[i][j]);
					if(k)
						dp[i][j][k] += dp[i][j-1][k-1] * p[i][j];
				}
			}
		}
		double ans1 = 1.0, ans2 = 1.0;
		for(int i=1; i<=n; i++) {
			double tmp = 0.0;
			for(int k=1; k<T; k++) {
				tmp += dp[i][m][k];
			}
			ans2 *= tmp;
			ans1 *= (1.0 - dp[i][m][0]);
		}
		printf("%.3f\n", ans1 - ans2);
	}
	return 0;
}

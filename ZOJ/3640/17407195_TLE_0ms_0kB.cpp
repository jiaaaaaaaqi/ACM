int c[maxn];
int t[maxn];
double dp[maxn][maxm];

void init() {
    mes(c, 0);
    mes(t, 0);
    mes(dp, 0);
}

void dfs(int id, int f) {
    if(dp[id][f])	return ;
    if(f > c[id]) {
        dp[id][f] = 1.0 * t[id];
        return ;
    }
    double ans = 0.0;
    for(int i=1; i<=n; i++) {
        dfs(i, f+c[id]);
        ans += dp[i][f+c[id]] + 1.0;
    }
    dp[id][f] = ans / n;
}

int main() {
    while(~scanf("%d%d", &n, &m)) {
        init();
        for(int i=1; i<=n; i++) {
            scanf("%d", &c[i]);
            t[i] = (int)((1.0 + sqrt(5.0)) / 2 * c[i] * c[i]);
        }
        double ans = 0.0;
        for(int i=1; i<=n; i++) {
            dfs(i, m);
            ans += dp[i][m];
        }
        printf("%.3f\n", ans / n);
    }
    return 0;
}

char s[maxn];

int main() {
	scanf("%d", &T);
	while(T--) {
		mes(s, 0);
		scanf("%s", s+1);
		int len = strlen(s+1);
		int ans = 0;
		int l = 1, r = len;
		int star = 0, num = 0;
		for(int i=1; i<=len; i++) {
			if(isdigit(s[i]))
				num++;
			else
				star++;
		}
		ans = max(star - num + 1, 0);
		star = 0;
		num = ans;
		for(; l<=len; l++) {
			if(isdigit(s[l])) {
				num++;
			} else {
				if(num < star + 2) {
					for(; r>l; r--) {
						if(isdigit(s[r]))
							break;
					}
					if(r > l) {
						swap(s[l], s[r]);
						num++;
						ans++;
					}
				} else {
					star++;
				}
			}
		}
		printf("%d\n", ans);
	}
	return 0;
}

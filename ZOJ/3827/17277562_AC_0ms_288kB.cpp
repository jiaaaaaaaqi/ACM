int main() {
	int t;
	scanf("%d", &t);
	while (t--) {
		int n;
		char str[20];
		scanf("%d%s", &n, str);
		double x, ans = 0;
		for (int i = 0 ; i < n; i++)
		{
			scanf("%lf", &x);
			x /= 100.0;
			if ((x - 0.0) <= eps) 
				continue;
			if (str[0] == 'b')
			{
				ans += x * log2(x);
			}
			else if (str[0] == 'n')
			{
				ans += x * log(x);
			}
			else
			{
				ans += x * log10(x);
			}
		}
		if(fabs(ans) <= eps)	printf("0.000000000000\n");
		else	printf("%.12f\n", -ans);
	}
	return 0;
}
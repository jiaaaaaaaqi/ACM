typedef unsigned long long int ull;
typedef long long int ll;
const int    maxn = 1e3 + 10;
const int    maxm = 1e5 + 10;
const ll     mod  = 1e9 + 7;
const ll     INF  = 1e18 + 100;
const int    inf  = 0x3f3f3f3f;
const double pi   = acos(-1.0);
const double eps  = 1e-8;
using namespace std;

int n, m;
int cas, tol, T;
ll dp[2][maxn][maxn];
int lcm[maxn][maxn];
vector<int> vv[maxn];

int main() {
    for(int i=1; i<=1000; i++) {
        for(int j=1; j<=i; j++) {
            lcm[i][j] = lcm[j][i] = i / __gcd(i, j) * j;
        }
    }
    for(m=1; m<=1000; m++) {
        for(int i=1; i*i<=m; i++) {
            if(m % i == 0) {
                vv[m].push_back(i);
                if(i * i != m)
                    vv[m].push_back(m/i);
            }
        }
        sort(vv[m].begin(), vv[m].end());
    }
    int K;
    while(~scanf("%d%d%d", &n, &m, &K)) {
        int flag = 0;
        dp[0][0][0] = 1;
        for(int k=1; k<=K; k++) {
            flag = !flag;
            for(int i=0; i<=n; i++) {
                for(int j=0; j<=m; j++) {
                    dp[flag][i][j] = 0;
                }
            }
            for(int i=0; i<=n; i++) {
                for(int j=0; j<=m; j++) {
                    for(auto it : vv[m]) {
                        if(i + it > n)	break;
                        int tmp = j==0 ? it : lcm[it][j];
                        if(tmp > m)		continue;
                        dp[flag][i+it][tmp] += dp[!flag][i][j];
                        dp[flag][i+it][tmp] %= mod;
                    }
                }
            }
        }
        printf("%lld\n", dp[flag][n][m]);
    }
    return 0;
}

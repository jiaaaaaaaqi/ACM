int n, m;
int cas, tol, T;

double p[maxn];
double a[maxn];
double b[maxn];

void init(int n) {
	mes(a, 0);
	mes(b, 0);
	mes(p, 0);
}

void dfs(int i) {
    if(i > n)
        return ;
    if(a[i] != 0.0)
        return ;
    for(int k=3; k<=tol; k++) {
        dfs(i+k);
        a[i] += a[i+k] * p[k];
        b[i] += b[i+k] * p[k];
    }
    a[i] += p[0];
    b[i] += 1;
}

int main() {
    scanf("%d", &T);
    while(T--) {
        int k1, k2, k3;
        int x, y, z;
        scanf("%d%d%d%d%d%d%d", &n, &k1, &k2, &k3, &x, &y, &z);
        init(k1+k2+k3);
        for(int i=1; i<=k1; i++) {
            for(int j=1; j<=k2; j++) {
                for(int k=1; k<=k3; k++) {
                    if(i == x && j == y && k == z)	continue;
                    p[i+j+k] += 1.0;
                }
            }
        }
        p[0] = 1.0;
        for(int i=0; i<=k1+k2+k3; i++) {
            p[i] = p[i] / (k1*k2*k3);
//			printf("%d %f\n", i, p[i]);
        }
        tol = k1+k2+k3;
        dfs(0);
        double ans = 1.0 * b[0] / (1 - a[0]);
        printf("%.15f\n", ans);
    }
    return 0;
}

typedef unsigned long long int ull;
typedef long long int ll;
const int    maxn = 1e3 + 10;
const int    maxm = 1e5 + 10;
const ll     mod  = 1e9 + 7;
const ll     INF  = 1e18 + 100;
const int    inf  = 0x3f3f3f3f;
const double pi   = acos(-1.0);
const double eps  = 1e-8;
using namespace std;

int n, m, v, x;
int cas, tol, T;

struct Node{
	int x, b;
	bool operator < (Node a) const {
		return x < a.x;
	}
} node[maxn];
ll dp[maxn][maxn][2], sum[maxn];

int main() {
	while(~scanf("%d%d%d", &n, &v, &x)) {
		int pos;
		mes(dp, inf);
		for(int i=1; i<=n; i++) {
			scanf("%d%d", &node[i].x, &node[i].b);
		}
		node[++n].x = x;
		node[n].b = 0;
		sort(node+1, node+1+n);
		sum[0] = 0;
		for(int i=1; i<=n; i++) {
			sum[i] = sum[i-1] + node[i].b;
			if(node[i].x == x) pos = i;
		}
		dp[pos][pos][0] = dp[pos][pos][1] = 0;
		for(int i=pos; i>=1; i--) {
			for(int j=pos; j<=n; j++) {
				if(i == j)	continue;
				dp[i][j][0] = min(dp[i][j][0], dp[i+1][j][0] + (node[i+1].x-node[i].x)*(sum[n]-sum[j]+sum[i]));
				dp[i][j][0] = min(dp[i][j][0], dp[i+1][j][1] + (node[j].x-node[i].x)*(sum[n]-sum[j]+sum[i]));
				dp[i][j][1] = min(dp[i][j][1], dp[i][j-1][0] + (node[j].x-node[i].x)*(sum[n]-sum[j-1]+sum[i-1]));
				dp[i][j][1] = min(dp[i][j][1], dp[i][j-1][1] + (node[j].x-node[j-1].x)*(sum[n]-sum[j-1]+sum[i-1]));
			}
		}
		printf("%lld\n", min(dp[1][n][0], dp[1][n][1])*v);
	}
	return 0;
}

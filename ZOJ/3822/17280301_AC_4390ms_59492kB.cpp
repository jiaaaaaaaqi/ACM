int T;

double dp[55][55][2505];

int main() {
    scanf("%d", &T);
    while(T--) {
		memset(dp, 0, sizeof(dp));
		dp[1][1][1] = 1;
		int n, m;
		scanf("%d%d", &n, &m);
		for (int i = 0; i <= n; i++) {
			for (int j = 0; j <= m; j++) {
				if (i == 0 || j == 0) continue;
				if (i == 1 && j == 1) continue;
				for (int k = max(i, j); k <= n*m; k++) {
				//	printf("i = %d j = %d k = %d\n", i, j, k);
					double k1, k2, k3, k4;
					int flag = n*m-(k-1);
					k1 = 1.0 * (n-(i-1))*j / flag;
					k2 = 1.0 * (m-(j-1))*i / flag;
					k3 = 1.0 * (n-(i-1))*(m-(j-1)) / flag;
					k4 = 1.0 * (i * j - (k-1)) / flag;
					if (i == n && j == m) k4 = 0;
					dp[i][j][k] = (k1 * dp[i-1][j][k-1] + k2*dp[i][j-1][k-1] + k3*dp[i-1][j-1][k-1] + k4*dp[i][j][k-1]);
				//	printf("k1 = %f k2 = %f k3 = %f k4 = %f\n", k1, k2, k3, k4);
				//	printf("dp[%d][%d][%d] = %f\n", i, j, k, dp[i][j][k]);
				}
			}
		}
		double ans = 0;
		for (int i = 0; i <= (min(m, n) * (max(n, m) - 1) + 1); i++)
			ans += i*dp[n][m][i];
		printf("%.12f\n", ans);
    }
    return 0;
}

int n, m;
int cas, tol, T;

double dp[55][55][2555];

int main() {
    scanf("%d", &T);
    while(T--) {
        scanf("%d%d", &n, &m);
        if(n > m)	swap(n, m);
        dp[0][0][0] = 1.0;
        for(int i=1; i<=n; i++) {
            for(int j=1; j<=m; j++) {
				for(int k=max(i, j); k<=i*j; k++) {
					int total = n * m - k + 1;
					double x1 = 1.0 * (n-(i-1)) * j/ total;
					double x2 = 1.0 * (m-(j-1)) * i / total;
					double x3 = 1.0 * (n-(i-1)) * (m-(j-1)) / total;
					double x4 = 1.0 * (i * j - (k-1)) / total;
					if(i == n && j == m)
						x4 = 0.0;
					dp[i][j][k] = x1 * dp[i-1][j][k-1] + x2 * dp[i][j-1][k-1] + x3 * dp[i-1][j-1][k-1] + x4 * dp[i][j][k-1];
				}
            }
        }
        double ans = 0.0;
        for(int i=m; i<=n*(m-1)+1; i++) {
            ans += i * dp[n][m][i];
        }
        printf("%.12f\n", ans);
    }
    return 0;
}

void mul(char *a) {
	char b[maxn];
	mes(b, 0);
	int len = strlen(a+1);
	for(int i=1; i<=len; i++) {
		b[i] = a[len-i+1];
	}
	for(int i=1; i<=len; i++) {
		b[i] = (b[i] - '0') * 2 + '0';
	}
	int flag = 0;
	for(int i=1; i<=len; i++) {
		if(b[i] > '9') {
			b[i] -= 10;
			if(i == len) {
				b[i+1] = '1';
				flag = 1;
			} else {
				b[i+1]++;
			}
		}
	}
	if(flag)	len++;
	mes(a, 0);
	for(int i=len; i>=1; i--) {
		a[i] = b[len - i + 1];
	}
}

int cmp(char *a, char *b) {
	int len1 = strlen(a+1);
	int len2 = strlen(b+1);
	if(len1 > len2)	return 1;
	if(len2 > len1)	return -1;
	for(int i=1; i<=len1; i++) {
		if(a[i] > b[i])	return 1;
		else if(a[i] < b[i])	return -1;
	}
	return 0;
}


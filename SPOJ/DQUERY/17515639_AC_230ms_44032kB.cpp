int n, m;
int cas, tol, T;

struct Node{
	int l, r;
	int sum;
} node[maxn * 40];
int a[maxn];
int rt[maxn];
int last[maxm];

void init() {
	tol = 0;
	mes(rt, 0);
	mes(last, -1);
}

void update(int l, int r, int &x, int y, int val, int pos) {
	tol++;
	node[tol] = node[y];
	node[tol].sum += val;
	x = tol;
	if(l == r)	
		return ;
	int mid = (l + r) >> 1;
	if(pos <= mid)
		update(l, mid, node[x].l, node[y].l, val, pos);
	else
		update(mid+1, r, node[x].r, node[y].r, val, pos);
}

int query(int l, int r, int pl, int pr, int rt) {
	if(pl <= l && r <= pr) {
		return node[rt].sum;
	}
	int mid = l + r >> 1;
	int ans = 0;
	if(pl <= mid)
		ans += query(l, mid, pl, pr, node[rt].l);
	if(pr > mid)
		ans += query(mid+1, r, pl, pr, node[rt].r);
	return ans;
}

int main() {
	while(~scanf("%d", &n)) {
		init();
		for(int i=1; i<=n; i++) {
			scanf("%d", &a[i]);
			if(last[a[i]] == -1) {
				update(1, n, rt[i], rt[i-1], 1, i);
			} else {
				int tmp;
				update(1, n, tmp, rt[i-1], -1, last[a[i]]);
				update(1, n, rt[i], tmp, 1, i);
			}
			last[a[i]] = i;
		}
		scanf("%d", &m);
		while(m--) {
			int l, r;
			scanf("%d%d", &l, &r);
			int ans = query(1, n, l, r, rt[r]);
			printf("%d\n", ans);
		}
	}
	return 0;
}

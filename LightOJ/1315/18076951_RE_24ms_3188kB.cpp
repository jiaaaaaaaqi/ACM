int n, m;
int cas, tol, T;

int dis[6][2] = {-2, 1, -2, -1, -3, -1, -1, -2, -1, -3, 1, -2};
vector<vector<int> > sg(maxn, vector<int>(maxn, -1));

int dfs(int x, int y) {
	if(sg[x][y] != -1)	return sg[x][y];
	vector<bool> vis(maxm, false);
	for(int i=0; i<6; i++) {
		int tx = x + dis[i][0];
		int ty = y + dis[i][1];
		if(tx < 1 || ty < 1)	continue;
		sg[tx][ty] = dfs(tx, ty);
		vis[sg[tx][ty]] = true;
	}
	for(int i=0; ; i++) {
		if(!vis[i]) {
			sg[x][y] = i;
			return i;
		}
	}
}

int main() {
	sg[1][1] = 0;
	for(int i=500; i>=1; i--) {
		for(int j=500; j>=1; j--) {
			if(sg[i][j] != -1)	continue;
			sg[i][j] = dfs(i, j);
		}
	}
	cas = 1;
	scanf("%d", &T);
	while(T--) {
		scanf("%d", &n);
		ll ans = 0;
		for(int i=1, x, y; i<=n; i++) {
			scanf("%d%d", &x, &y);
			x++, y++;
			ans ^= sg[x][y];
		}
		printf("Case %d: %s\n", cas++, ans ? "Alice" : "Bob");
	}
	return 0;
}

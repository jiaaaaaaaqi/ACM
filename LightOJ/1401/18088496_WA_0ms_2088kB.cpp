int n, m;
int cas, tol, T;

char s[maxn];
int sg[maxn][3][3];

void handle() {
	mes(sg, 0);
	for(int i=1; i<=100; i++) {
		for(int l=0; l<3; l++) {
			for(int r=0; r<3; r++) {
				vector<bool> vis(maxn, false);
				for(int p=1; p<=i; p++) {
					if((!(p==1 && l==1)) && (!(p==i && r==1))) {
						vis[sg[p-1][l][1] ^ sg[i-p][1][r]] = true;
					}
					if((!(p==1 && l==2)) && (!(p==i && r==2))) {
						vis[sg[p-1][l][2] ^ sg[i-p][2][r]] = true;
					}
				}
				for(int j=0; ; j++) {
					if(!vis[j]) {
						sg[i][l][r] = j;
						break;
					}
				}
			}
		}
	}
}

int main() {
	handle();
	cas = 1;
	scanf("%d", &T);
	while(T--) {
		scanf("%s", s+1);
		int len = strlen(s+1);
		s[len+1] = '.';
		int cnt = 0, ans = 0;
		int l = 0, r, last=1;
		for(int i=1; i<=len+1; i++) {
			if(i == len+1) {
				r = 0;
				ans ^= sg[i-last][l][r];
//				printf("%d %d %d %d\n", i-last, l, r, sg[i-last][l][r]);
			} else if(s[i] != '.') {
				cnt++;
				r = s[i]=='X' ? 1 : 2;
				ans ^= sg[i-last][l][r];
//				printf("%d %d %d %d\n", i-last, l, r, sg[i-last][l][r]);
				l = r;
				last = i+1;
			}
		}
		if(cnt&1) {
			if(ans) printf("Case %d: No", cas++);
			else	printf("Case %d: Yes", cas++);
		} else {
			if(ans) printf("Case %d: Yes", cas++);
			else	printf("Case %d: No", cas++);
		}
	}
	return 0;
}

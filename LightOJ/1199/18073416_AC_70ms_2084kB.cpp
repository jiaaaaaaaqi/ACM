int n, m;
int cas, tol, T;

vector<int> sg(maxn, 0);

void get_sg(int n) {
	sg[0] = 0;
	for(int i=1; i<=n; ++i) {
		vector<bool> vis(maxn, false);
		for(int j=1; j<=i/2; ++j) {
			int l = j, r = i-j;
			if(l == r)	continue;
			vis[sg[l] ^ sg[r]] = true;
		}
		for(int j=0; ; ++j) {
			if(!vis[j]) {
				sg[i] = j;
				break;
			}
		}
	}
}

int main() {
	cas = 1;
    get_sg(10000);
    scanf("%d", &T);
    while(T--) {
		scanf("%d", &n);
		int ans = 0;
		for(int i=1, x; i<=n; i++) {
			scanf("%d", &x);
			ans ^= sg[x];
		}
		printf("Case %d: %s\n", cas++, ans ? "Alice" : "Bob");
    }
    return 0;
}

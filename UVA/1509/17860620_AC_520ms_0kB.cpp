char s1[maxn], s2[maxn];
map<char, string> mp;
bool flag;

void dfs(int id1, int id2, int len1, int len2) {
	if(flag)	return ;
	if(id1 == len1+1 && id2 == len2+1) {
		flag = true;
		return ;
	}
	int a1 = len1 - id1 + 1;
	int b1 = len2 - id2 + 1;
	if(a1 > b1 || a1*n <b1) {
		return ;
	}
	if(id1 >= len1+1 || id2 >= len2+1) {
		return ;
	}
	string s = "";
	string ss = "";
	if(mp.count(s1[id1])) {
		ss = mp[s1[id1]];
		int len = ss.size();
		for(int i=0; i<len && id2+i<=len2; i++) {
			s += s2[id2+i];
		}
		if(s == ss) {
			dfs(id1+1, id2+len, len1, len2);
			if(flag)	return ;
		}
		else
			return ;
	} else {
		for(int i=0; i<n && id2+i<=len2; i++) {
			s += s2[id2+i];
			mp.insert(make_pair(s1[id1], s));
			dfs(id1+1, id2+i+1, len1, len2);
			if(flag)	return ;
			mp.erase(s1[id1]);
		}
	}
}

int main() {
	scanf("%d", &T);
	while(T--) {
		mp.clear();
		mes(s1, 0), mes(s2, 0);
		scanf("%d", &n);
		scanf("%s%s", s1+1, s2+1);
		flag = false;
		dfs(1, 1, strlen(s1+1), strlen(s2+1));
		printf("%d\n", flag);
	}
	return 0;
}

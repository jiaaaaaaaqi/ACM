int n, m;
int cas, tol, T;

struct Node{
	double l, r;
	bool operator < (Node a) const {
		return l != a.l ? l < a.l : r > a.r;
	}
} node[maxn];

double calc(double r, double w) {
	double ans = r*r - w*w/4;
	return sqrt(ans);
}

int main() {
	double l, w;
	while(~scanf("%d%lf%lf", &m, &l, &w)) {
		n = 0;
		for(int i=1; i<=m; i++) {
			double p, r;
			scanf("%lf%lf", &p, &r);
			if(2*r < w)	continue;
			n++;
			double dis = calc(r, w);
			node[n].l = 1.0*p - dis;
			if(node[n].l < 0)	node[n].l = 0.0;
			node[n].r = 1.0*p + dis;
		}
		sort(node+1, node+1+n);
//		for(int i=1; i<=n; i++) {
//			printf("%f %f\n", node[i].l, node[i].r);
//		}
		if(node[1].l > 0) {
			printf("-1\n");
			continue;
		}
		int ans = 0;
		double en = 0.0, be = 0.0;
		for(int i=1; i<=n; ) {
			while(i<=n && node[i].l <= be) {
				en = max(en, node[i].r);
				i++;
			}
//			printf("!!!%f %f\n", be, en);
			ans++;
			be = en;
			if(be >= l || i > n)	break;
		}
		if(en < l) {
			printf("-1\n");
			continue;
		}
		printf("%d\n", ans);
	}
	return 0;
}



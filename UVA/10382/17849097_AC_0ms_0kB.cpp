int n, m;
int cas, tol, T;

struct Node{
	double l, r;
	bool operator < (Node a) const {
		return l != a.l ? l < a.l : r < a.r;
	}
} node[maxn];

void init() {
	n = 0;
	mes(node, 0);
}

double calc(double r, double w) {
	return sqrt(r*r - w*w/4);
}

int main() {
	double l, w;
	while(~scanf("%d%lf%lf", &m, &l, &w)) {
		init();
		for(int i=1; i<=m; i++) {
			double p, r;
			scanf("%lf%lf", &p, &r);
			if(2*r < w)	continue;
			double d = calc(r, w);
			++n;
			node[n].l = p-d;
			node[n].r = p+d;
		}
		sort(node+1, node+1+n);
//		for(int i=1; i<=n; i++) {
//			printf("%f %f\n", node[i].l, node[i].r);
//		}
		if(node[1].l > 0) {
			printf("-1\n");
			continue;
		}
		double last, now;
		last = now = 0.0;
		int ans = 0;
		for(int i=1; i<=n; ) {
			bool flag = false;
			while(node[i].l <= last && i<=n) {
				flag = true;
				now = max(now, node[i].r);
				i++;
			}
			if(!flag) {
				break;
			}
			ans++;
			last = now;
			if(now >= l)	break;
		}
		if(now < l) {
			printf("-1\n");
		} else {
			printf("%d\n", ans);
		}
	}
	return 0;
}

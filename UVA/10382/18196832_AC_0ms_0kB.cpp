int n, m;
int cas, tol, T;

struct Node {
    double l, r;
    bool operator < (Node a) const {
        return l != a.l ? l < a.l : r < a.r;
    }
} node[maxn];

void init() {
    n = 0;
    mes(node, 0);
}

double calc(double r, double w) {	//计算三角形横着的直角边长度
    return sqrt(r*r - w*w/4);
}

int main() {
    double l, w;
    while(~scanf("%d%lf%lf", &m, &l, &w)) {
        init();
        for(int i=1; i<=m; i++) {
            double p, r;
            scanf("%lf%lf", &p, &r);
            if(2*r < w)	continue;	//如果碰不到长方形上下边，可以不考虑
            double d = calc(r, w);
            node[++n].l = p-d;	//圆在长方形内的有效长度
            node[n].r = p+d;
        }
        sort(node+1, node+1+n);
        if(node[1].l > 0) {
            printf("-1\n");
            continue;
        }
        double last, now;
        last = now = 0.0;
        int ans = 0;
        for(int i=1; i<=n; ) {
            bool flag = false;
            while(node[i].l <= last && i<=n) {
                flag = true;
                now = max(now, node[i].r);	//贪心可以覆盖到的最远长度
                i++;
            }
            if(!flag) 	break;	//表示中间存在空隙
            ans++;
            last = now;
            if(now >= l)	break;	//已经填满了长方形
        }
        if(now < l)	printf("-1\n");
        else	printf("%d\n", ans);
    }
    return 0;
}

using namespace std;

int n, m, k;
int cas, tol, T;

struct Node{
	string s;
	int cnt;
	int A, B;
	bool operator < (Node a) const {
		return cnt != a.cnt ? cnt < a.cnt : s<a.s;
	}
} node[maxn];
char s[maxn];

void solve(int id) {
	node[id].s = "";
	node[id].A = node[id].B = 0;
	int len = strlen(s+1);
	bool flag = false;
	int i=1;
	for(; i<=len; i++) {
		if(s[i] == ':') {
			i++;
			break;
		}
		node[id].s += s[i];
	}
	for(; i<=len; i++) {
		if(s[i] == ',') {
			i++;
			break;
		}
		node[id].A = node[id].A*10 + s[i]-'0';
	}
	for(; i<=len; i++) {
		if(s[i] == ',') {
			i++;
			break;
		}
		node[id].B = node[id].B*10 + s[i]-'0';
	}
}

void calc(int id) {
	int ans = 0;
	int tmp = n;
	while(tmp/2 >= m) {
		ans += min((tmp+1)/2*node[id].A, node[id].B);
		tmp /= 2;
	}
	ans += (tmp-m)*node[id].A;
	node[id].cnt = ans;
}

int main() {
	cas = 1;
	scanf("%d", &T);
	while(T--) {
		scanf("%d%d%d", &n, &m, &k);
		for(int i=1; i<=k; i++) {
			mes(s, 0);
			scanf("%s", s+1);
			solve(i);
			calc(i);
		}
		sort(node+1, node+1+k);
		printf("Case %d\n", cas++);
		for(int i=1; i<=k; i++) {
			cout << node[i].s << " " << node[i].cnt << endl;
		}
	}
	return 0;
}

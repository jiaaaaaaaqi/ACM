int dp[maxn][maxn];
int path[maxn][maxn];

int main() {
    while(~scanf("%d%d", &m, &n)) {
        mes(v, 0);
        mes(dp, 0);
        for(int i=1; i<=n; i++) {
            scanf("%d", &v[i]);
        }
        for(int i=1; i<=n; i++) {
            for(int j=0; j<=m; j++) {
                if(j < v[i]) {
                    dp[i][j] = dp[i-1][j];
                    path[i][j] = j;
                } else {
                    dp[i][j] = max(dp[i-1][j], dp[i-1][j-v[i]] + v[i]);
                    if(dp[i-1][j] > dp[i-1][j-v[i]] + v[i])
                        path[i][j] = j;
                    else
                        path[i][j] = j-v[i];
                }
            }
        }
        int ans = dp[n][m];
        tol = 0;
//        for(int i=1; i<=n; i++) {
//            for(int j=1; j<=m; j++) {
//                printf("%d%c", path[i][j], j==m ? '\n' : ' ');
//            }
//        }
        while(n) {
            if(path[n][m] != m) {
                a[++tol] = v[n];
            }
            m = path[n][m];
            n--;
        }
        for(int i=tol; i>=1; i--)
            printf("%d ", a[i]);
        printf("sum:%d\n", ans);
    }
    return 0;
}

int n, m;
int cas, tol, T;

struct Node{
	int n1, n2, n3, n4, n5;
} node[maxn];
int a[maxn];
int ans;

void dfs(int id, int cnt) {
	if(id == n+1)	return ;
	if(cnt > m)		return ;
	if(n-id < m-cnt)	return ;
	if(cnt == m) {
		int r1, r2, r3, r4, r5;
		r1 = r2 = r3 = r4 = r5 = 0;
		for(int i=1; i<=m; i++) {
			r1 = max(r1, node[a[i]].n1);
			r2 = max(r2, node[a[i]].n2);
			r3 = max(r3, node[a[i]].n3);
			r4 = max(r4, node[a[i]].n4);
			r5 = max(r5, node[a[i]].n5);
		}
		ans = max(ans, r1+r2+r3+r4+r5);
		return ;
	}
	dfs(id+1, cnt);
	a[cnt+1] = id;
	dfs(id+1, cnt+1);
	a[cnt+1] = 0;
}

int main() {
	scanf("%d", &T);
	while(T--) {
		mes(a, 0);
		scanf("%d%d", &n, &m);
		for(int i=1; i<=n; i++) {
			scanf("%d%d%d%d%d", &node[i].n1, &node[i].n2, &node[i].n3, &node[i].n4, &node[i].n5);
		}
		ans = 0;
		dfs(0, 0);
		printf("%d\n", ans);
	}
	return 0;
}

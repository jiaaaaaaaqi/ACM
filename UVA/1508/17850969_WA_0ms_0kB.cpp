struct Node{
	int a[6];
} node[maxn];
int mx[50];
int ans;

void dfs(int u, int cnt, int sum) {
	if(cnt == m) {
		ans = max(ans, sum);
		return ;
	}
	for(int i=u; i; i=(i-1)&u) {
		dfs(i^u, cnt+1, sum + mx[i]);
	}
}

int main() {
    scanf("%d", &T);
    while(T--) {
		mes(mx, 0);
        scanf("%d%d", &n, &m);
        for(int i=1; i<=n; i++) {
			for(int j=1; j<=5; j++) {
				scanf("%d", &node[i].a[j]);
			}
        }
        if(m >= 5) {
			int r1, r2, r3, r4, r5;
			for(int i=1; i<=n; i++) {
				for(int j=1; j<=5; j++) {
					r1 = max(r1, node[i].a[1]);
					r2 = max(r2, node[i].a[2]);
					r3 = max(r3, node[i].a[3]);
					r4 = max(r4, node[i].a[4]);
					r5 = max(r5, node[i].a[5]);
				}
			}
			printf("%d\n", r1+r2+r3+r4+r5);
			continue;
        } else {
			for(int i=1; i<=n; i++) {
				for(int u=0; u<=31; u++) {
					int sum = 0;
					for(int j=1; j<=5; j++) {
						if(u & (1<<(j-1))) {
							sum += node[i].a[j];
						}
					}
					mx[u] = max(mx[u], sum);
				}
			}
			ans = 0;
			dfs(31, 0, 0);
			printf("%d\n", ans);
        }
    }
    return 0;
}

double ans;
double a[10];
bool vis[10];
int x[10];

double calc(double R, double r) {
	double ans = (R+r)*(R+r) - (R-r)*(R-r);
	return sqrt(ans);
}

bool dfs(int id, int cnt) {
	if(cnt == n) {
		double res = a[x[1]] + a[x[n]];
		for(int i=2; i<=n; i++) {
			res += calc(a[x[i]], a[x[i-1]]);
		}
		ans = min(ans, res);
		return true;
	} 
	if(cnt == 1) {
		for(int i=1; i<=n; i++) {
			if(vis[i])	continue;
			vis[i] = 1;
			x[cnt+1] = i;
			dfs(i, cnt+1);
			vis[i] = 0;
			x[cnt+1] = 0;
		}
	} else {
		if(x[cnt-1] < x[cnt]) {
			for(int i=1; i<=x[cnt]-1; i++) {
				if(vis[i])	continue;
				vis[i] = 1;
				x[cnt+1] = i;
				dfs(i, cnt+1);
				vis[i] = 0;
				x[cnt+1] = 0;
			}
		} else {
			for(int i=x[cnt]+1; i<=n; i++) {
				if(vis[i])	continue;
				vis[i] = 1;
				x[cnt+1] = i;
				dfs(i, cnt+1);
				vis[i] = 0;
				x[cnt+1] = 0;
			}
		}
	}
}

int main() {
	scanf("%d", &T);
	while(T--) {
		mes(a, 0);
		ans = 1.0 * inf;
		scanf("%d", &n);
		for(int i=1; i<=n; i++) {
			scanf("%lf", &a[i]);
		}
		sort(a+1, a+1+n);
		for(int i=1; i<=n; i++) {
			mes(vis, 0);
			x[1] = i;
			vis[i] = 1;
			dfs(i, 1);
			x[1] = 0;
			vis[i] = 0;
		}
		printf("%.3f\n", ans);
	}
	return 0;
}

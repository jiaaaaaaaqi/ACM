using namespace std;

int n, m;
int cas, tol, T;

struct Node{
	int l, r;
	bool operator < (Node a) const {
		return l < a.l;
	}
} node[maxn];
int ans[maxn];

void init() {
	mes(ans, 0);
}

int main() {
	scanf("%d", &T);
	while(T--) {
		init();
		scanf("%d", &m);
		int a, b;
		n = 0;
		while(scanf("%d%d", &a, &b), a||b) {
			if(b < 0)	continue;
			if(a > m)	continue;
			n++;
			node[n].l = a;
			node[n].r = b;
		}
		sort(node+1, node+1+n);
//		for(int i=1; i<=n; i++) {
//			printf("!!%d %d\n", node[i].l, node[i].r);
//		}
		tol = 1;
		int last, now;
		last = now = 0;
		for(int i=1; i<=n; tol++) {
			bool flag = false;
			while(node[i].l <= last && i<=n) {
				if(node[i].r > now) {
					ans[tol] = i;
					now = node[i].r;
					flag = true;
				}
				i++;
			}
			if(!flag)		break;
			last = now;
			if(now >= m)	break;
		}
		if(now >= m) {
			printf("%d\n", tol);
			for(int i=1; i<=tol; i++) {
				printf("%d %d\n", node[ans[i]].l, node[ans[i]].r);
			}
		} else {
			printf("0\n");
		}
		if(T)	printf("\n");
	}
	return 0;
}
